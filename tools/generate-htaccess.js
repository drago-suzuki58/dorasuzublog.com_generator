function normalizePath(value, fieldName) {
  if (typeof value !== 'string' || value.length === 0) {
    throw new Error(`${fieldName} must be a non-empty string`);
  }

  let pathname;
  try {
    pathname = new URL(value, 'https://dorasuzublog.com').pathname;
  } catch {
    throw new Error(`${fieldName} is not a valid URL path: ${value}`);
  }

  if (!pathname.startsWith('/')) {
    pathname = `/${pathname}`;
  }

  return pathname;
}

function loadRedirects() {
  const redirects = JSON.parse(fs.readFileSync(REDIRECTS_FILE, 'utf8'));
  if (!Array.isArray(redirects)) {
    throw new Error('redirects.json must be an array');
  }

  const seen = new Set();
  return redirects.map((redirect, index) => {
    const from = normalizePath(redirect.from, `redirects[${index}].from`);
    const to = normalizePath(redirect.to, `redirects[${index}].to`);

    if (from === to) {
      throw new Error(`Redirect source and target are identical: ${from}`);
    }

    if (seen.has(from)) {
      throw new Error(`Duplicate redirect source: ${from}`);
    }
    seen.add(from);

    return { from, to };
  });
}

function renderHtaccess(redirects) {
  const lines = [
    '# Generated by tools/generate-htaccess.js. Do not edit manually.',
    '# Edit tools/redirects.json, then run npm run redirects:generate.',
    '',
    '<IfModule mod_rewrite.c>',
    'RewriteEngine On',
    '',
    'RewriteCond %{HTTPS} !=on',
    'RewriteRule ^ https://%{HTTP_HOST}%{REQUEST_URI} [R=301,L]',
    '',
    '# Remove legacy AMP suffixes.',
    'RewriteRule ^(.+)/amp/?$ /$1/ [R=301,L,NE]',
    '',
    '# Send common scanner/probe URLs to the teapot page.',
    ...TEAPOT_REDIRECT_RULES.map(rule => `RewriteRule ${rule} /418/ [R=302,L,NC]`),
    '',
    'RewriteRule ^404/?$ - [R=404,L]',
    'RewriteRule ^403/?$ - [F,L]',
    '</IfModule>',
    '',
    'ErrorDocument 404 /404/index.html',
    'ErrorDocument 403 /403/index.html',
    ''
  ];

  for (const { from, to } of redirects) {
    lines.push(`Redirect 301 ${from} ${to}`);
  }

  lines.push('');
  return lines.join('\n');
}

function main() {
  const redirects = loadRedirects();
  const htaccess = renderHtaccess(redirects);
  const current = fs.existsSync(OUTPUT_FILE)
    ? fs.readFileSync(OUTPUT_FILE, 'utf8')
    : '';

  if (CHECK_ONLY) {
    if (current !== htaccess) {
      console.error('source/.htaccess is out of date. Run npm run redirects:generate.');
      process.exitCode = 1;
    }
    return;
  }

  fs.writeFileSync(OUTPUT_FILE, htaccess, 'utf8');
  console.log(`Generated redirects: ${redirects.length}, teapot redirects: ${TEAPOT_REDIRECT_RULES.length}`);
}

main();
